var DIFF_DELETE = -1;
var DIFF_INSERT = 1;
var DIFF_EQUAL = 0;

function get_merged_diff_patch(a, b, o) {
    var a_diff = get_diff_patch(o, a)
    var b_diff = get_diff_patch(o, b)
    var ds = []
    var prev_d = null
    while (a_diff.length > 0 || b_diff.length > 0) {
        var d = a_diff.length == 0 ?
            b_diff.shift() :
            b_diff.length == 0 ?
                a_diff.shift() :
                a_diff[0][0] < b_diff[0][0] ?
                    a_diff.shift() :
                    a_diff[0][0] > b_diff[0][0] ?
                        b_diff.shift() :
                        a_diff[0][2] < b_diff[0][2] ?
                            a_diff.shift() :
                            b_diff.shift()
        if (prev_d && d[0] < prev_d[0] + prev_d[1]) {
            if (d[0] + d[1] > prev_d[0] + prev_d[1]) {
                prev_d[1] = d[0] + d[1] - prev_d[0]
            }
            prev_d[2] += d[2]
        } else {
            ds.push(d)
            prev_d = d
        }
    }
    return ds
}

function apply_diff_patch(s, diff) {
    var offset = 0
    for (var i = 0; i < diff.length; i++) {
        var d = diff[i]
        s = s.slice(0, d[0] + offset) + d[2] + s.slice(d[0] + offset + d[1])
        offset += d[2].length - d[1]
    }
    return s
}

function diff_convert_to_my_format(d) {
    var x = []
    var ii = 0
    for (var i = 0; i < d.length; i++) {
        var dd = d[i]
        if (dd[0] == DIFF_EQUAL) {
            ii += dd[1].length
            continue
        }
        var xx = [ii, 0, '']
        if (dd[0] == DIFF_INSERT) {
            xx[2] = dd[1]
        } else if (dd[0] == DIFF_DELETE) {
            xx[1] = dd[1].length
            ii += xx[1]
        }
        if (i + 1 < d.length) {
            dd = d[i + 1]
            if (dd[0] != DIFF_EQUAL) {
                if (dd[0] == DIFF_INSERT) {
                    xx[2] = dd[1]
                } else if (dd[0] == DIFF_DELETE) {
                    xx[1] = dd[1].length
                    ii += xx[1]
                }
                i++
            }
        }
        x.push(xx)
    }
    return x
}

function get_diff_patch(a, b) {
    return diff_convert_to_my_format(diff_main(a, b))
}

diffsync.get_diff_patch = get_diff_patch

